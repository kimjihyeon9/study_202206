package Q001;

public class Q007 {
	/*
	 자바의 기본적인 코드 구성에서 각 키워드를 간략히 설명해보시오
	 (자바코드의 주요 키워드들에 대해서 각각의 의미와 쓰임새(용도)에 대해서 아는지를 묻는 문제이다)
	 (간략한 개념 정도만 알면 된다. 더 알 수도 없다)
	 
	 [소스코드]
	 public class Java100_variable_HelloJava{
	 	public static void main(String[] args){
	 		System.out.println("Hello Java~ ><");
	 	}
	 }
	 
	 =>
	 public class Java100_variable_HelloJava
	 public : 접근 제한자
	 class : 클래스 선언
	 Java100_variable_HelloJava : 클래스 이름
	 
	 {} : 블럭
	 
	 public static void main(String[] args)
	 public : 접근제한자
	 void : 반환타입
	 main : 메서드 이름
	 (String[] args) : 파라미터
	 
	 System.out.println("Hello Java~ ><"); : 구현할 코드 작성
	 
	 접근제한자 클래스선언 클래스이름{
	 	접근제한자 static 반환타입 메서드이름(파라미터){
	 		구현할 코드 작성
	 	}
	 }
	 
	 > 접근제한자
	 - public, private, protected, default
	 - 클래스나 메서드에 접근할 수 있는 범위를 지정 -> 접근제한자, 접근제어자
	 - private > protected > public
	 
	 > 클래스선언
	 - class 
	 - 객체를 생성하는 툴, 프레임, 공장, 템플릿
	 
	 > 클래스이름
	 - 카멜케이스 (단어와 단어 사이의 구분 -> 대문자)
	 
	 > 메서드이름
	 - 메서드 : 함수, 어떤 특정한 동작이나 작업, 행위 등을 수행하는 것
	 */
}



















